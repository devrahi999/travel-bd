enum AppLanguage { bn, en }

final localeProvider = StateNotifierProvider<LocaleNotifier, AppLanguage>((ref) {
  return LocaleNotifier();
});

class LocaleNotifier extends StateNotifier<AppLanguage> {
  LocaleNotifier() : super(AppLanguage.bn) {
    _loadLocale();
  }

  static const _key = 'app_language';

  Future<void> _loadLocale() async {
    final prefs = await SharedPreferences.getInstance();
    final lang = prefs.getString(_key);
    if (lang == 'en') {
      state = AppLanguage.en;
    } else {
      state = AppLanguage.bn;
    }
  }

  Future<void> setLanguage(AppLanguage language) async {
    state = language;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_key, language == AppLanguage.en ? 'en' : 'bn');
  }

  String translate(String key) {
    return _translations[state]?[key] ?? key;
